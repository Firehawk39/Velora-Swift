import Foundation
import Security
import Security

struct VeloraCredentialsBundle: Codable {
    var serverUrl: String
    var onlineServerUrl: String
    var username: String
    var connectionMode: Int
}

@MainActor
final class KeychainHelper {
    static let shared = KeychainHelper()
    private init() {}

    func save(_ data: Data, service: String, account: String) {
        let query = [
            kSecValueData: data,
            kSecClass: kSecClassGenericPassword,
            kSecAttrService: service,
            kSecAttrAccount: account
        ] as CFDictionary

        // Add data in keychain to be sure it's saved
        var status = SecItemAdd(query, nil)

        if status == errSecDuplicateItem {
            // Item already exists, thus update it.
            let query = [
                kSecAttrService: service,
                kSecAttrAccount: account,
                kSecClass: kSecClassGenericPassword
            ] as CFDictionary

            let attributesToUpdate = [kSecValueData: data] as CFDictionary

            status = SecItemUpdate(query, attributesToUpdate)
        }

        if status != errSecSuccess {
            AppLogger.shared.log("Error saving to Keychain: \(status, level: .error)")
        }
    }

    func read(service: String, account: String) -> Data? {
        let query = [
            kSecAttrService: service,
            kSecAttrAccount: account,
            kSecClass: kSecClassGenericPassword,
            kSecReturnData: true
        ] as CFDictionary

        var result: AnyObject?
        let status = SecItemCopyMatching(query, &result)

        if status == errSecSuccess {
            return result as? Data
        }
        return nil
    }

    func delete(service: String, account: String) {
        let query = [
            kSecAttrService: service,
            kSecAttrAccount: account,
            kSecClass: kSecClassGenericPassword
        ] as CFDictionary

        SecItemDelete(query)
    }
}
